// Unified preventive fiscal scan — rulează toate detectoarele preventive
// disponibile (filings deadline radar, cert expiry, frequency mismatch,
// consistency, sequence gap dacă date prezente) într-un singur swoop și
// întoarce rezultat agregat + listă de findings new gata pentru persistare.
//
// "Preventiv" = catch BEFORE ANAF returns a rejection. Spre deosebire de
// SPV signals (reactive, post-fact), aceste detectoare lucrează pe state-ul
// local: orgProfile, filingRecords, certSpvRecords. Nu necesită token ANAF
// OAuth valid — funcționează în orice mediu (mock/test/prod).
//
// Pure-ish: funcția primește state + nowISO + opts, întoarce rezultat.
// Persistarea findings → state e responsabilitatea caller-ului (route handler).

